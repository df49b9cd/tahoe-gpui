//! Spatial layout foundation (HIG Spatial layout).
//!
//! Platform: **visionOS only**. Spatial layout covers 3D placement,
//! ornaments, volumes, and shared space vs full space — concepts with
//! no analogue in GPUI on macOS today. Documented here so HIG audits
//! find an anchor.
//!
//! # See also
//!
//! - [`crate::foundations::layout`] — the 2D layout tokens (margins,
//!   content width) that macOS apps use instead of spatial conventions.
//! - [`crate::foundations::immersive_experiences`] — the adjacent
//!   visionOS foundation covering full-space scene rendering.
//!
//! # HIG Reference
//!
//! <https://developer.apple.com/design/human-interface-guidelines/spatial-layout>
//!
