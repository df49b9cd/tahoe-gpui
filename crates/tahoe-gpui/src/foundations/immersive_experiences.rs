//! Immersive experiences foundation (HIG Immersive experiences).
//!
//! Platform: **visionOS primary**. Full-space scenes, passthrough
//! blending, and immersion-style transitions are visionOS concepts
//! rendered via RealityKit — outside GPUI's 2D element tree. Adjacent
//! guidance for macOS Tahoe's Liquid Glass surfaces is covered in
//! [`crate::foundations::materials`]; this module is documented for
//! HIG taxonomy completeness only.
//!
//! # See also
//!
//! - [`crate::foundations::materials`] — Liquid Glass tokens, the
//!   closest macOS analogue to immersive visionOS scene materials.
//! - [`crate::foundations::spatial_layout`] — visionOS spatial layout,
//!   paired with this foundation.
//!
//! # HIG Reference
//!
//! <https://developer.apple.com/design/human-interface-guidelines/immersive-experiences>
//!
//! Tracked by `docs/hig/foundations.md:588`.
